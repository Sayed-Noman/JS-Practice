/* Compound Operators */

// Addition Assignment Operators
let x = 10;
x += 5;  // Equivalent to x = x + 5
console.log(x);  // Output: 15

// Subtraction Assignment Operator
let y = 20;
y -= 8;  // Equivalent to y = y - 8
console.log(y);  // Output: 12

// Multiplication Assisgnment Operator
let z = 4;
z *= 3;  // Equivalent to z = z * 3
console.log(z);  // Output: 12

// Division Assignment Operator
let a = 30;
a /= 6;  // Equivalent to a = a / 6
console.log(a);  // Output: 5

// Modulo Assignment Operator
let b = 17;
b %= 5;  // Equivalent to b = b % 5
console.log(b);  // Output: 2

// Exponentiation Assignment Operator
let c = 2;
c **= 4;  // Equivalent to c = c ** 4
console.log(c);  // Output: 16

// Bitwise AND Assignment Operator
let d = 14;  // Binary: 1110
d &= 7;      // Binary: 0111
console.log(d);  // Output: 6 (Binary: 0110)

// Bitwise OR Assignmen Operator
let e = 4;   // Binary: 0100
e |= 7;      // Binary: 0111
console.log(e);  // Output: 7 (Binary: 0111)

// Bitwise XOR Assignment Operator
let f = 12;  // Binary: 1100
f ^= 7;      // Binary: 0111
console.log(f);  // Output: 9 (Binary: 1001)

// Bitwise Left Shift Assignment Operator
let g = 3;   // Binary: 0011
g <<= 2;     // Shift left by 2 bits
console.log(g);  // Output: 12 (Binary: 1100)

// Bitwise Right Shift Assignment Operator
let h = 32;  // Binary: 00100000
h >>= 3;     // Shift right by 3 bits
console.log(h);  // Output: 4 (Binary: 00000100)

// Increment Operator

let currentAge = 25;

// Prefix increment
console.log(++currentAge);  // Output: 26 (currentAge is incremented to 26, then 26 is returned)

// Postfix increment
console.log(currentAge++);  // Output: 26 (current value of currentAge is 26, then currentAge is incremented to 27)

// Check the final value of 'currentAge'
console.log(currentAge);    // Output: 27

// Decrement Operator

let remainingTasks = 8;

// Prefix decrement
console.log(--remainingTasks);  // Output: 7 (remainingTasks is decremented to 7, then 7 is returned)

// Postfix decrement
console.log(remainingTasks--);  // Output: 7 (current value of remainingTasks is 7, then remainingTasks is decremented to 6)

// Check the final value of 'remainingTasks'
console.log(remainingTasks);    // Output: 6


 
